   public javax.swing.ComboBoxModel<java.lang.String> getLista()
    {
        PantallaPrincipalUsuario u= new PantallaPrincipalUsuario();
        productos= u.cargarAppPP().listarProductos();
         if(productos==null)
         {
             System.out.print("hollaa");
         }
         try
         {
         String[] catalogo =new String[productos.size()];
         int i=0;
        
         for(Producto p: productos)
         {
             if(p.isEstado())
             {
                 catalogo[i]= p.getName();
                 
                 i++;
             }
         }
        JComboBox<String> n= new JComboBox<String>(catalogo);
       
        this.jComboBox1=n;
         }catch(Exception e)
         {
             System.out.print(e.getMessage());
         }
        return jComboBox1.getModel();
    }
